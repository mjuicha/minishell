#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	x;
	size_t	i;
	char	*dest;

	x = 0;
	while (s1[x])
		x++;
	x++;
	dest = (char *)malloc(sizeof(char) * x);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
