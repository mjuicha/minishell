#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	x;

	x = 0;
	while (s[x] != (char)c)
	{
		if (s[x] == '\0')
			return (0);
		x++;
	}
	return ((char *)&s[x]);
}
